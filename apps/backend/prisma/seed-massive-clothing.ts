async function main() {
  console.log("开始种子数据填充...\n");

  console.log("=== 创建品牌 ===");
  const brandMap = new Map<string, string>();
  
  for (const brandData of BRANDS) {
    const brand = await prisma.brand.upsert({
      where: { slug: brandData.slug },
      update: {
        name: brandData.name,
        description: brandData.description,
        priceRange: brandData.priceRange,
        isActive: true,
      },
      create: {
        name: brandData.name,
        slug: brandData.slug,
        description: brandData.description,
        priceRange: brandData.priceRange,
        isActive: true,
      },
    });
    brandMap.set(brandData.name, brand.id);
    console.log(`  ✓ ${brandData.name} (${brandData.slug})`);
  }

  console.log("\n=== 清理现有服装数据 ===");
  await prisma.clothingItem.deleteMany({});
  console.log("  ✓ 已清理");

  const allItems = [
    ...TOPS_ITEMS,
    ...BOTTOMS_ITEMS,
    ...DRESSES_ITEMS,
    ...OUTERWEAR_ITEMS,
    ...FOOTWEAR_ITEMS,
    ...ACCESSORIES_ITEMS,
    ...ACTIVEWEAR_ITEMS,
    ...SWIMWEAR_ITEMS,
  ];

  console.log(`\n=== 创建服装商品 (${allItems.length} 件) ===`);

  const brandNames = Array.from(brandMap.keys());
  let created = 0;

  for (const item of allItems) {
    const randomBrandName = brandNames[Math.floor(Math.random() * brandNames.length)];
    const brandId = randomBrandName ? (brandMap.get(randomBrandName) ?? null) : null;

    await prisma.clothingItem.create({
      data: {
        name: item.name,
        description: item.description,
        category: item.category,
        subcategory: item.subcategory,
        price: item.price,
        originalPrice: item.originalPrice,
        colors: item.colors,
        sizes: item.sizes,
        tags: item.tags,
        images: item.images,
        mainImage: item.mainImage,
        brandId: brandId,
        stock: item.stock || 100,
        isActive: true,
        isFeatured: Math.random() > 0.8,
      },
    });
    created++;
    
    if (created % 20 === 0) {
      console.log(`  已创建 ${created}/${allItems.length} 件...`);
    }
  }

  console.log(`  ✓ 共创建 ${created} 件商品`);

  const countByCategory = await prisma.clothingItem.groupBy({
    by: ["category"],
    _count: true,
  });

  console.log("\n=== 各分类统计 ===");
  for (const row of countByCategory) {
    console.log(`  ${row.category}: ${row._count} 件`);
  }

  const totalItems = await prisma.clothingItem.count();
  const totalBrands = await prisma.brand.count();

  console.log("\n=== 填充完成 ===");
  console.log(`  品牌: ${totalBrands} 个`);
  console.log(`  商品: ${totalItems} 件`);
}

main()
  .catch((e) => {
    console.error("种子数据填充失败:", e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
